   _testptime("sys: ", res.sys);
   _testptime("\t\t sys: ", res.sys / (double)j->iters);
   fprintf(_test_realout, "\n");
   _testptestout("STDOUT: ", 0, 1);
   _testptestout("\x1B[30;41mSTDERR\x1B[0m: ", 1, 1);
